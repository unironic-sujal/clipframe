function getInitialTheme() {
  const saved = localStorage.getItem('theme');
  if (saved) return saved;
  // Default to system preference
  if (window.matchMedia && window.matchMedia('(prefers-color-scheme: light)').matches) {
    return 'light';
  }
  return 'dark'; // Default
}


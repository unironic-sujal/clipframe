/**
 * Capture the current video frame and download as a lossless PNG.
 * Canvas is sized to the video's native resolution (e.g. 3840Ãƒâ€”2160).
 */
function extractFrame() {
  if (!state.file || state.duration === 0) return;

  videoEl.pause();

  const ctx = captureCanvas.getContext('2d');
  ctx.drawImage(videoEl, 0, 0, captureCanvas.width, captureCanvas.height);

  captureCanvas.toBlob(blob => {
    if (!blob) {
      showToast('Frame capture failed. Try again.', 'error');
      return;
    }

    const blobUrl = URL.createObjectURL(blob);
    const anchor  = document.createElement('a');

    // SECURITY: Sanitize filename Ã¢â‚¬â€ strip extension and non-safe chars
    const rawName  = state.file.name.replace(/\.[^.]+$/, '');
    const safeName = rawName.replace(/[^a-zA-Z0-9_\-]/g, '_').substring(0, 60);
    const timeStr  = formatTime(videoEl.currentTime)
      .replace(/:/g, '-')
      .replace('.', '-');
    const frameNum = Math.round(videoEl.currentTime * state.fps);

    anchor.href     = blobUrl;
    anchor.download = `ClipFrame_${safeName}_${timeStr}_f${frameNum}.png`;

    // Programmatic click to trigger download
    document.body.appendChild(anchor);
    anchor.click();
    document.body.removeChild(anchor);

    // Revoke blob URL after a short delay
    setTimeout(() => URL.revokeObjectURL(blobUrl), 10000);

    showToast(`Saved: ${anchor.download}`, 'success');
  }, 'image/png'); // lossless PNG
}


// Captures the current frame at 1/4 resolution, encodes it as PNG,
// measures the blob, then scales by pixel-count ratio to the full size.
// Much more accurate than the old formula Ã¢â‚¬â€ changes per frame as expected.

const SIZE_PROXY_MAX_W = 480; // proxy width cap Ã¢â‚¬â€ fast to encode, good ratio proxy
let   sizeEstimateTimer = null;
const proxyCanvas = document.createElement('canvas');
const proxyCtx    = proxyCanvas.getContext('2d');

function scheduleSizeEstimate() {
  if (sizeEstimateTimer) clearTimeout(sizeEstimateTimer);
  sizeEstimateTimer = setTimeout(computeSizeEstimate, 500);
}

function computeSizeEstimate() {
  if (!state.videoLoaded || state.nativeWidth === 0) return;

  const scale  = Math.min(1, SIZE_PROXY_MAX_W / state.nativeWidth);
  const proxyW = Math.round(state.nativeWidth  * scale);
  const proxyH = Math.round(state.nativeHeight * scale);

  proxyCanvas.width  = proxyW;
  proxyCanvas.height = proxyH;
  proxyCtx.drawImage(videoEl, 0, 0, proxyW, proxyH);

  // Show a brief "measuring" state while the async blob is computed
  infoPngSize.textContent = 'measuring...';

  proxyCanvas.toBlob(blob => {
    if (!blob || !infoPngSize) return;

    // Scale proxy blob size up to full resolution by pixel count ratio
    const pixelRatio   = (state.nativeWidth * state.nativeHeight) / (proxyW * proxyH);
    const fullEstimate = blob.size * pixelRatio;
    const mb           = fullEstimate / (1024 * 1024);

    infoPngSize.textContent = mb >= 1
      ? `~${mb.toFixed(1)} MB`
      : `~${Math.round(fullEstimate / 1024)} KB`;
  }, 'image/png');
}



/**
 * Truncate a string to maxLen characters.
 * @param {string} str @param {number} maxLen @returns {string}
 */
function truncateStr(str, maxLen) {
  if (str.length <= maxLen) return str;
  const ext = str.lastIndexOf('.');
  if (ext > 0) {
    const name = str.substring(0, ext);
    const extension = str.substring(ext);
    return name.substring(0, maxLen - extension.length - 1) + 'Ã¢â‚¬Â¦' + extension;
  }
  return str.substring(0, maxLen - 1) + 'Ã¢â‚¬Â¦';
}


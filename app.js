/**
 * Attempt to detect the video's native framerate using a ghost video element
 * and requestVideoFrameCallback, which fires on each rendered frame.
 * Measures inter-frame mediaTime intervals across 8 seeks.
 * Falls back silently if the API isn't available (Firefox).
 */
async function autoDetectFPS() {
  // requestVideoFrameCallback is Chrome 83+ / Edge 83+ only
  if (!('requestVideoFrameCallback' in HTMLVideoElement.prototype)) return;
  if (state.duration < 1) return;

  const ghost = document.createElement('video');
  ghost.src         = state.objectUrl;
  ghost.muted       = true;
  ghost.preload     = 'auto';
  ghost.playsInline = true;
  ghost.style.cssText = 'position:absolute;left:-99999px;top:-99999px;width:1px;height:1px;visibility:hidden;pointer-events:none;';
  document.body.appendChild(ghost);

  // Wait for ghost metadata
  try {
    await new Promise((resolve, reject) => {
      const t = setTimeout(() => reject(), 8000);
      ghost.addEventListener('loadedmetadata', () => { clearTimeout(t); resolve(); }, { once: true });
      ghost.addEventListener('error',          () => { clearTimeout(t); reject();  }, { once: true });
      ghost.load();
    });
  } catch (_) {
    ghost.src = ''; ghost.remove();
    return;
  }

  // Seek to a stable position away from the very start (skip intro/black frames)
  const startPos = Math.min(2, state.duration / 4);
  ghost.currentTime = startPos;
  await new Promise(resolve => {
    ghost.addEventListener('seeked', resolve, { once: true });
    setTimeout(resolve, 3000); // fallback if seeked never fires
  });

  // Ã¢â€â‚¬Ã¢â€â‚¬ Play and collect frame timestamps Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬Ã¢â€â‚¬
  // requestVideoFrameCallback fires ONCE PER RENDERED FRAME during playback.
  // Collecting 14 consecutive frame times gives 13 intervals Ã¢â€ â€™ reliable median.
  const frameTimes = [];

  const detected = await new Promise(resolve => {
    const timeout = setTimeout(() => { ghost.pause(); resolve(null); }, 5000);

    const onFrame = (now, meta) => {
      frameTimes.push(meta.mediaTime);

      if (frameTimes.length >= 14) {
        clearTimeout(timeout);
        ghost.pause();

        // Calculate inter-frame intervals
        const intervals = [];
        for (let i = 1; i < frameTimes.length; i++) {
          const d = frameTimes[i] - frameTimes[i - 1];
          // Valid range: 200fps (0.005s) to 10fps (0.1s)
          if (d > 0.004 && d < 0.11) intervals.push(d);
        }
        if (intervals.length < 5) { resolve(null); return; }

        // Median is more robust than mean against decoder hiccups
        intervals.sort((a, b) => a - b);
        const median = intervals[Math.floor(intervals.length / 2)];
        resolve(1 / median);
      } else {
        ghost.requestVideoFrameCallback(onFrame);
      }
    };

    ghost.requestVideoFrameCallback(onFrame);
    ghost.play().catch(() => { clearTimeout(timeout); resolve(null); });
  });

  ghost.src = ''; ghost.remove();

  if (!detected || detected < 10 || detected > 300) return;

  // Round to the nearest standard framerate
  const STANDARD = [23.976, 24, 25, 29.97, 30, 48, 50, 59.94, 60, 120];
  const nearest  = STANDARD.reduce((a, b) =>
    Math.abs(b - detected) < Math.abs(a - detected) ? b : a
  );
  if (Math.abs(nearest - detected) > 3) return; // too far off Ã¢â‚¬â€ don't guess

  const rounded = Math.round(nearest); // 23.976Ã¢â€ â€™24, 29.97Ã¢â€ â€™30 etc.

  // Add the option if not already present
  const existing = Array.from(fpsSelect.options).map(o => parseInt(o.value, 10));
  if (!existing.includes(rounded)) {
    const opt = document.createElement('option');
    opt.value       = String(rounded);
    opt.textContent = String(rounded);
    const insertBefore = Array.from(fpsSelect.options).find(o => parseInt(o.value, 10) > rounded);
    fpsSelect.insertBefore(opt, insertBefore || null);
  }

  fpsSelect.value = String(rounded);
  state.fps       = rounded;
  scrubberEl.step = String(1 / rounded);
  infoFps.textContent = `${rounded} fps`;
  updateFrameInfo();

  // Show the green "auto" badge
  const badge = $('fps-auto-badge');
  if (badge) badge.classList.remove('hidden');
}


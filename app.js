function togglePlay() {
  if (!state.videoLoaded && state.duration === 0) return;
  if (videoEl.paused || videoEl.ended) {
    videoEl.play().catch(() => showToast('Playback failed. Try clicking play again.', 'error'));
  } else {
    videoEl.pause();
  }
}

/**
 * Step by N frames (positive = forward, negative = backward).
 * @param {number} frames - Number of frames to step
 */
function stepFrames(frames) {
  if (state.duration === 0) return;
  videoEl.pause();
  const frameDuration = 1 / state.fps;
  const newTime = Math.max(0, Math.min(state.duration, videoEl.currentTime + frames * frameDuration));
  videoEl.currentTime = newTime;
}


/**
 * Load a local video file. Validates MIME type before creating object URL.
 * @param {File} file
 */
function loadFile(file) {
  if (!file) return;

  // SECURITY: Validate file type — check MIME type (primary) OR extension (fallback).
  // Some browsers on Windows return an empty file.type for .mov / .mkv / .avi even
  // though the file is a valid video. Extension check is the safety net.
  const KNOWN_VIDEO_EXT = /\.(mp4|m4v|mov|webm|mkv|avi|wmv|flv|ogv|3gp|3g2|ts|mts|m2ts|mpg|mpeg)$/i;
  const isVideoMime = file.type.startsWith('video/');
  const isVideoExt  = KNOWN_VIDEO_EXT.test(file.name);

  if (!isVideoMime && !isVideoExt) {
    showToast('Please select a video file (MP4, MOV, WebM, MKV…)', 'error');
    return;
  }

  // Revoke previous object URL to free memory
  if (state.objectUrl) {
    URL.revokeObjectURL(state.objectUrl);
    state.objectUrl = null;
  }

  state.file = file;
  state.objectUrl = URL.createObjectURL(file);
  state.videoLoaded = false;

  // Reset framerate badge — will be re-detected for the new video
  const badge = $('fps-auto-badge');
  if (badge) badge.classList.add('hidden');
  fpsSelect.value = '30';
  state.fps = 30;

  videoEl.src = state.objectUrl;
  videoEl.load();

  // Show player, hide drop zone
  dropZone.classList.add('hidden');
  playerSection.classList.remove('hidden');

  // Update filename display safely (textContent, not innerHTML)
  infoFilename.textContent = truncateStr(file.name, 36);
  infoFilename.title = file.name;
}


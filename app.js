function updateResolutionInfo() {
  const w = state.nativeWidth;
  const h = state.nativeHeight;

  let label = `${w}\u00D7${h}`;
  if (h >= 4320)       label = '8K';
  else if (h >= 2160)  label = '4K';
  else if (h >= 1440)  label = '2K';
  else if (h >= 1080)  label = 'FHD';
  else if (h >= 720)   label = 'HD';
  else                 label = 'SD';

  infoResLabel.textContent   = label;
  infoDimensions.textContent = `${w} \u00D7 ${h}`;
  infoDuration.textContent   = formatTime(state.duration);
  infoFps.textContent        = `${state.fps} fps`;
}


/**
 * Copy the current frame to the system clipboard as PNG.
 * Requires Clipboard API (Chrome 76+, not available on iOS Safari).
 */
async function copyToClipboard() {
  if (!state.file || state.duration === 0) return;

  videoEl.pause();

  const ctx = captureCanvas.getContext('2d');
  ctx.drawImage(videoEl, 0, 0, captureCanvas.width, captureCanvas.height);

  captureCanvas.toBlob(async blob => {
    if (!blob) {
      showToast('Frame capture failed.', 'error');
      return;
    }
    try {
      await navigator.clipboard.write([
        new ClipboardItem({ 'image/png': blob }),
      ]);
      showToast('Frame copied to clipboard!', 'success');
    } catch (_) {
      showToast('Clipboard not available in this browser.', 'error');
    }
  }, 'image/png');
}

